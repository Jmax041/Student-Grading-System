    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed

        if(emailText.getText().isEmpty() || passwordText.getText().isEmpty() || fnameText.getText().isEmpty() || lnameText.getText().isEmpty() || addressText.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Some information is missing!");
        }
        
        else if (emailAlreadyExists(emailText.getText())) {
            JOptionPane.showMessageDialog(this, "Email already exists!");
        }

        else{

            try{
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gradedb","root","");
                PreparedStatement add = con.prepareStatement("INSERT INTO account VALUES(?,?,?,?,?,?,?)");
                add.setInt(1,0);
                add.setString(2,emailText.getText());
                add.setString(3,passwordText.getText());
                add.setString(4,fnameText.getText());
                add.setString(5,lnameText.getText());
                add.setString(6,addressText.getText());
                add.setString(7,birthChooser.getDate().toString());

                add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Account saved successfully!");
                con.close();
                clearText();
                new front_Page().setVisible(true);
                this.dispose();
            }

            catch(Exception e){

                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_submitActionPerformed

