    private boolean emailAlreadyExists(String email) {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gradedb", "root", "");
            PreparedStatement statement = con.prepareStatement("SELECT COUNT(*) FROM account WHERE email = ?");
            statement.setString(1, email);
            ResultSet resultSet = statement.executeQuery();
            resultSet.next();
            int count = resultSet.getInt(1);
            con.close();
            return count > 0;
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error checking email existence: " + e.getMessage());
            return false;
        }
    }
    
