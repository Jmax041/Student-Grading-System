    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        if(newGradeText.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "New grade is missing!");
        }

        else if(!newGradeText.getText().matches("\\d+")){
            JOptionPane.showMessageDialog(this, "New grade must contain only numeric characters!");
        }
        
        else if (Integer.parseInt(newGradeText.getText()) < 0){
            JOptionPane.showMessageDialog(this, "New grade cannot be a negative number!");
        }
        
        else if (Integer.parseInt(newGradeText.getText()) > 100){
            JOptionPane.showMessageDialog(this, "New grade cannot be more than 100!");
        }

        else{
            
            try{
                
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gradedb", "root", "");
                String update = "UPDATE student SET grade = ? WHERE name = ? AND teacherID = ?";
                PreparedStatement statement = con.prepareStatement(update);
                statement.setInt(1, Integer.parseInt(newGradeText.getText()));  // New grade value
                statement.setString(2, myStudentName);  // Student name
                statement.setInt(3, myID);  // Teacher ID
                
                statement.executeUpdate();
                JOptionPane.showMessageDialog(this, "Student grade updated successfully!");
                getDate();
                getTime();
                print();
                con.close();
                clearText();
                
                String retrieve = "SELECT * FROM account where email ='"+ myEmail +"' ";
                try{

                    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gradedb","root","");
                    sm = con.createStatement();
                    rs = sm.executeQuery(retrieve);

                    if(rs.next()){

                        new update_Grade(rs.getInt(1), rs.getString(2)).setVisible(true);
                        this.dispose();
                    }

                }

                catch(Exception e){

                    JOptionPane.showMessageDialog(this, e);
                }
            }
            
            catch(Exception e){

                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

