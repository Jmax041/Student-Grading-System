    private void getDate(){
        
        Date d= new Date();
        SimpleDateFormat sdf =  new SimpleDateFormat("MM-dd-yyyy");
        transDate = sdf.format(d);
    }
    
    private void getTime(){
        
        Date t= new Date();
        SimpleDateFormat sdf =  new SimpleDateFormat("hh:mm:ss a");
        transTime = sdf.format(t);
    }
    
